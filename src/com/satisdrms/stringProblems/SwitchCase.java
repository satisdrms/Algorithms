package com.satisdrms.stringProblems;

public class SwitchCase {

	public static void main(String[] args) {
		String s = "sa";
		switch (s) {
		case "da":
			System.out.println("Found da");
			break;
		case "sa":
			System.out.println("Found sa");
			break;
		default:
			System.out.println("Going to default");
		}

	}

}
